package com.learning.protobuff_basics.serializers;

public class SelfSimpleMessage {

	private int id;
	
	private String message;

	public SelfSimpleMessage(int id, String message) {
		super();
		this.id = id;
		this.message = message;
	}

	public SelfSimpleMessage() {
		super();
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getMessage() {
		return message;
	}

	public void setMessage(String message) {
		this.message = message;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + id;
		result = prime * result + ((message == null) ? 0 : message.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		SelfSimpleMessage other = (SelfSimpleMessage) obj;
		if (id != other.id)
			return false;
		if (message == null) {
			if (other.message != null)
				return false;
		} else if (!message.equals(other.message))
			return false;
		return true;
	}

	@Override
	public String toString() {
		return "SelfSimpleMessage [id=" + id + ", message=" + message + "]";
	}
	
	
	
}
